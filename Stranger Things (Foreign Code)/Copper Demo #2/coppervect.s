
**	RASTER LIGHTSOURCED VECTOR
**	CODED BY CRADON/MATRIX
**	ASM-ONE FORMAT

*******************************************************************************

	SECTION	A,CODE_C

START	MOVEM.L	D0-A6,-(A7)
	MOVE.W	$DFF01C,INTENA
	MOVE.W	$DFF002,DMACON
	MOVE.W	$DFF010,ADKCON
	MOVE.W	#%0111111111111111,$DFF096
	MOVE.W	#%0111111111111111,$DFF09A
	MOVE.W	#%0111111111111111,$DFF09C
	JSR	INITRAS
	JSR	INITCOL
	MOVE.L 	$6C,OLDIRQ
	MOVE.L 	#MYIRQ,$6C
	MOVE.W	#%1000001111000000,$DFF096
	MOVE.W 	#%1100000000110000,$DFF09A
	JSR	INITCOP
LOOP	BTST	#6,$BFE001
	BNE.B	LOOP
ENDE	MOVE.W	#%0111111111111111,$DFF096
	MOVE.W	#%0111111111111111,$DFF09A
	MOVE.W	#%0111111111111111,$DFF09C
	MOVE.L	OLDIRQ(PC),$00006C
	MOVE.W	DMACON(PC),D0			
	OR.W	#$8000,D0			
	MOVE.W	D0,$DFF096
	MOVE.W	ADKCON(PC),D0			
	OR.W	#$8000,D0
	MOVE.W	D0,$DFF09E
	MOVE.W	INTENA(PC),D0			
	OR.W	#$C000,D0
	MOVE.W	D0,$DFF09A
	MOVE.W	#0,$DFF088
	MOVEM.L	(A7)+,D0-A6
	RTS

** MAKE COPPERLIST SELFACTIVATE ***********************************************

INITCOP	MOVE.L	#COPPER1,D0
	LEA.L	ADRESS2,A0
	MOVE.W	D0,6(A0)
	SWAP	D0
	MOVE.W	D0,2(A0)
	LEA.L	NUMBER,A0
	MOVE.W	PLANER(PC),D0
	ROR.W	#4,D0
	OR.W	D0,2(A0)	
	MOVE.L	#COPPER1,$DFF084
	MOVE.B	#$00,$DFF08A
	RTS

** INIT rasterlines IN COPPERLIST *********************************************

INITRAS	LEA.L	RASTER,A0
	MOVE.W	rasterlines,D0
	MOVE.W	STARTRAST,D2

RASLOP	MOVE.W	D2,(A0)+
	MOVE.W	#$FF00,(A0)+
	MOVE.W	#$0182,(A0)+
	MOVE.W	#$0000,(A0)+
	MOVE.W	#$0184,(A0)+
	MOVE.W	#$0000,(A0)+
	MOVE.W	#$0188,(A0)+
	MOVE.W	#$0000,(A0)+
	ADD.W	#$100,D2
	SUBQ.W	#1,D0
	BNE	RASLOP
	RTS	

** UPDATE RASTER COLOURS ******************************************************

rastcol	MOVEM.L	D0-A6,-(A7)
	CMP.W	#0,D0
	BEQ	FIRST
	CMP.W	#1,D0
	BEQ	SECOND
	CMP.W	#2,D0
	BEQ	THIRD
RASOUT	MOVEM.L	(A7)+,D0-A6
	RTS

FIRST	LEA.L	DESTIN(PC),A4
	LEA.L	RASTER,A5
	EXT.L	D2	
	DIVU	#12,D2
	EXT.L	D2	
	LSL.W	#1,D2
	MOVE.w	rasterlines,D1
WRITE0	MOVE.W	(A4,D2.W),6(A5)
	ADD.L	#20,A4
	ADD.L	#16,A5	
	SUBQ.W	#1,D1
	BNE	WRITE0
	BRA.S	RASOUT

SECOND	LEA.L	DESTIN(PC),A4
	LEA.L	RASTER,A5
	EXT.L	D2	
	DIVU	#12,D2
	EXT.L	D2	
	LSL.W	#1,D2
	MOVE.w	rasterlines,D1
WRITE1	MOVE.W	(A4,D2.W),$A(A5)
	ADD.L	#20,A4
	ADD.L	#16,A5	
	SUBQ.W	#1,D1
	BNE	WRITE1
	BRA.S	RASOUT

THIRD	LEA.L	DESTIN(PC),A4
	LEA.L	RASTER,A5
	EXT.L	D2	
	DIVU	#12,D2
	EXT.L	D2	
	LSL.W	#1,D2
	MOVE.w	rasterlines,D1
WRITE2	MOVE.W	(A4,D2.W),$E(A5)
	ADD.L	#20,A4
	ADD.L	#16,A5	
	SUBQ.W	#1,D1
	BNE	WRITE2
	BRA.W	RASOUT

** RASTER COLOURS *************************************************************

INITCOL	LEA.L	SOURCE(PC),A0
	LEA.L	DESTIN(PC),A1
	MOVE.W	rasterlines,D3
COLMAIN	MOVE.W	(A0)+,D0
	MOVEQ	#10,D2
LOP	MOVE	D0,(A1)+
	MOVE.W	D0,D1
	AND.W	#$000F,D1
	CMP.W	#$F,D1
	BEQ	.GREEN
	ADDQ	#1,D1
	AND.W	#$0FF0,D0
	OR	D1,D0	
.GREEN	MOVE.W	D0,D1
	AND.W	#$00F0,D1
	LSR.W	#4,D1
	CMP.W	#$F,D1
	BEQ	.RED
	ADDQ	#1,D1
	LSL.W	#4,D1
	AND.W	#$0F0F,D0
	OR	D1,D0
.RED	MOVE.W	D0,D1
	AND.W	#$0F00,D1
	LSR.W	#8,D1
	CMP.W	#$F,D1
	BEQ	.NEXT
	ADDQ	#1,D1
	LSL.W	#8,D1
	AND.W	#$00FF,D0
	OR	D1,D0
.NEXT	SUBQ.W	#1,D2
	BNE	LOP
	SUBQ.W	#1,D3
	BNE	COLMAIN
	RTS

*******************************************************************************
****************************** FILLED VECTOR **********************************
*******************************************************************************

VECTOR	MOVE.W	#320,GLOBALXMIN
	MOVE.W	#0,GLOBALXMAX
	MOVE.W	#256,GLOBALYMIN
	MOVE.W	#0,GLOBALYMAX
	JSR	ANGLES
	LEA.L	FIGURE,A1
	LEA.L	SIN_TABLE,A2
	LEA.L	COS_TABLE,A3
VLOOP	MOVE.L	SCREEN_ADRESS,A0
	MOVE.W	(A1)+,D0
	CMP.W	#$FFFF,D0
	BEQ.W	VECOUT
	MOVE.W	(A1)+,D1
	MOVE.W	(A1)+,D2
	BSR.W	NORMAL		; ROTATE VECTORNORMAL (0-XN,0-YN,0-ZN)
	MOVE.W	(A1)+,D3
	CMP.W	D3,D2		; FRONTSIDE OF PLANE?
	BGT.S	.PLANE
	MOVE.W	(A1)+,D0
	EXT.L	D0
	LSL.W	#3,D0
	ADDQ	#2,D0
	ADD.L	D0,A1		; POINT AT NEXT PLANE
	BRA.S	VLOOP
.PLANE	MOVE.W	(A1)+,COUNTER	; NUMBER OF POINTS
	MOVE.W	(A1)+,D0	; SELECTED COLOUR
	MOVE.W	#10240,D1
	MULU	D0,D1
	ADD.L	D1,A0

	jsr	rastcol

LINES	MOVE.W	(A1)+,D0
	MOVE.W	(A1)+,D1
	MOVE.W	(A1)+,D2
	SUB.W	XCENTER(PC),D0	; CENTER OBJECT
	SUB.W	YCENTER(PC),D1
	SUB.W	ZCENTER(PC),D2
	BSR.B	ROTATE		; ROTATE 3D POINT
	ADD.W	ZCENTER(PC),D2
	JSR	CONVERT		; CONVERT FROM 3D TO 2D
	ADD.W	XCENTER(PC),D0
	ADD.W	YCENTER(PC),D1
	JSR	EXTREM		; NOTE PLANE MAX AND MIN
	JSR	LINE		; DRAW LINE FROM LAST POINT
	SUB.W	#1,COUNTER
	BNE.B	LINES
	JSR	FILL		; FILL PLANE
	JSR	GLOBAL		; NOTE GLOBAL EXTRIEMES (CLEAR)
	MOVE.W	#256,YMIN	; RESET FOR NEXT PLANE
	MOVE.W	#0,YMAX
	MOVE.W	#320,XMIN
	MOVE.W	#0,XMAX
	BRA.W	VLOOP		; WORK ON NEXT PLANE
VECOUT	RTS	

** ROTATE *********************************************************************

ROTATE	MOVE.W	XVINKEL(PC),D5			; X ANGLE
	MOVE.W	YVINKEL(PC),D6			; Y ANGLE
	MOVE.W	ZVINKEL(PC),D7			; Z ANGLE
	LSL.W	#1,D5				; X ANGLE *2
	LSL.W	#1,D6				; Y ANGLE *2
	LSL.W	#1,D7				; Z ANGLE *2
	MOVE.W	(A3,D5.W),D3			; COS(XGRAD)
	MOVE.W	(A2,D5.W),D4			; SIN(XGRAD)
	MULS	D1,D3				; Y*COS(XGRAD)
	MULS	D2,D4				; Z*SIN(XGRAD)
	SUB.L	D4,D3				; Y*COS(XGRAD) - Z*SIN(XGRAD)
	LSL.L	#2,D3				; 4*(Y*COS(XGRAD)-Z*SIN(XGRAD))
	MOVE.L	D3,Y1				; Y-COORDINATE AROUND X AXIS
	MOVE.W	(A2,D5.W),D3			; SIN(XGRAD)
	MOVE.W	(A3,D5.W),D4			; COS(XGRAD)
	MULS	D1,D3				; Y*SIN(XGRAD)
	MULS	D2,D4				; Z*COS(XGRAD)
	ADD.L	D4,D3				; Y*SIN(XGRAD) + Z*COS(XGRAD)
	LSL.L	#2,D3				; 4*(Y*SIN(XGRAD)+Z*COS(XGRAD))
	MOVE.L	D3,Z1				; Z-COORDINATE AROUND X AXIS
	MOVE.W	(A3,D6.W),D3			; COS(YGRAD)
	MOVE.W	(A2,D6.W),D4			; SIN(YGRAD)
	MULS	D0,D3				; X*COS(YGRAD)
	MULS	Z1(PC),D4			; Z*SIN(YGRAD)
	SUB.L	D4,D3				; X*COS(YGRAD) - Z*SIN(YGRAD)
	LSL.L	#2,D3				; 4*(X*COS(YGRAD)-Z*SIN(YGRAD))
	MOVE.L	D3,X2				; X-COORDINATE AROUND Y AXIS
	MOVE.W	(A2,D6.W),D2			; SIN(YGRAD)
	MOVE.W	(A3,D6.W),D4			; COS(YGRAD)
	MULS	D0,D2				; X*SIN(YGRAD)
	MULS	Z1(PC),D4			; Z*COS(YGRAD)
	ADD.L	D4,D2				; X*SIN(YGRAD) + Z*COS(YGRAD)
	LSL.L	#2,D2				; 4*(X*SIN(YGRAD)+Z*COS(YGRAD))
	SWAP	D2				; FAST DIVISION BY 65535
	EXT.L	D2				; Z-COORDINATE AROUND Y AND X
	MOVE.W	(A3,D7.W),D0			; COS(ZGRAD)
	MOVE.W	(A2,D7.W),D4			; SIN(ZGRAD)
	MULS	X2(PC),D0			; X*COS(ZGRAD)
	MULS	Y1(PC),D4			; Y*SIN(ZGRAD)
	SUB.L	D4,D0				; X*COS(ZGRAD) - Y*SIN(ZGRAD)
	LSL.L	#2,D0				; 4*(X*COS(ZGRAD)-Y*SIN(ZGRAD))
	SWAP	D0				; FAST DIVISION BY 65535
	EXT.L	D0				; X-COORDINATE AROUND Y AND Z
	MOVE.W	(A2,D7.W),D1			; SIN(ZGRAD)
	MOVE.W	(A3,D7.W),D3			; COS(ZGRAD)
	MULS	X2(PC),D1			; X*SIN(ZGRAD)
	MULS	Y1(PC),D3			; Y*COS(ZGRAD)
	ADD.L	D3,D1				; X*SIN(ZGRAD) + Y*COS(ZGRAD)
	LSL.L	#2,D1				; 4*(X*SIN(ZGRAD)+Y*COS(ZGRAD))
	SWAP	D1				; FAST DIVISION BY 65535
	EXT.L	D1				; Y-COORDINATE AROUND X AND Z
	RTS

** ROTATE PLANES VECTOR NORMAL ************************************************

NORMAL	MOVE.W	XVINKEL(PC),D5
	MOVE.W	YVINKEL(PC),D6
	LSL.W	#1,D5
	LSL.W	#1,D6
	MOVE.W	(A2,D5.W),D3
	MOVE.W	(A3,D5.W),D4
	MULS	D1,D3
	MULS	D2,D4
	ADD.L	D4,D3
	LSL.L	#2,D3
	MOVE.L	D3,Z1				; Z ROTATED AROUND X
	MOVE.W	(A2,D6.W),D2
	MOVE.W	(A3,D6.W),D4
	MULS	D0,D2
	MULS	Z1(PC),D4
	ADD.L	D4,D2
	LSL.L	#2,D2
	SWAP	D2
	EXT.L	D2				; Z ROTATED AROUND Y AND X
	RTS

** CONVERT POINT FROM 3D TO 2D ************************************************

CONVERT	MULS	ZOOM(PC),D0
	MULS	ZOOM(PC),D1
	DIVS	D2,D0
	DIVS	D2,D1
	RTS

** DRAW LINE ******************************************************************

LINE	TST.W	(A1)+				; DRAW LINE FROM LAST POINT?
	BNE.B	DRAW
	MOVE.W	D0,LASTX			; NO! THEN JUST SAVE THIS POINT
	MOVE.W	D1,LASTY
	RTS
DRAW	MOVE.W	LASTX(PC),D2
	MOVE.W	LASTY(PC),D3
	MOVE.W	D0,LASTX
	MOVE.W	D1,LASTY
	EXG	D1,D2	
	CMP	D2,D3
	BEQ.W	DRAWOUT
	BPL.B	BLINE
	EXG	D0,D1
	EXG	D2,D3
BLINE	SUBQ	#1,D3
	MOVE	D1,D6
	SUB	D0,D6
	BMI.S	XL0
	MOVE.W	D3,D7
	SUB.W	D2,D7
	BMI.S	XG0YL0
	MOVE.W	D7,D5
	SUB.W	D6,D5
	BMI.S	O4
	MOVE.B	#1,BLTC1
	BRA.S	OCTOK
O4	MOVE.B	#17,BLTC1
	BRA.S	OCTOK
XG0YL0	NEG.W	D7
	MOVE.W	D7,D5
	SUB.W	D6,D5
	BMI.S	O6
	MOVE.B	#5,BLTC1
	BRA.S	OCTOK
O6	MOVE.B	#25,BLTC1
	BRA.S	OCTOK
XL0	NEG.W	D6
	MOVE.W	D3,D7
	SUB.W	D2,D7
	BMI.S	XL0YL0
	MOVE.W	D7,D5
	SUB.W	D6,D5
	BMI.S	O5
	MOVE.B	#9,BLTC1
	BRA.S	OCTOK
O5	MOVE.B	#21,BLTC1
	BRA.S	OCTOK
XL0YL0	NEG.W	D7
	MOVE.W	D7,D5
	SUB.W	D6,D5
	BMI.S	O7
	MOVE.B	#13,BLTC1
	BRA.S	OCTOK
O7	MOVE.B	#29,BLTC1
OCTOK	MOVEQ	#0,D4
	MOVE.W	D0,D4
	AND.W	#15,D4
	SWAP	D4
	LSR.L	#4,D4
	OR.W	#$B4A,D4
	MOVEQ	#0,D3
	LSL	#3,D2
	MOVE	D2,D3
	LSL	#2,D2
	ADD	D2,D3
	LSR	#3,D0
	AND.B	#$FE,D0
	ADD	D0,D3
	ADD.L	A0,D3
	TST	D5
	BMI.S	XMAXI
	EXG	D6,D7
	NEG.W	D5
XMAXI	MOVE.W	D6,D1
	ADD.W	#1,D1
	LSL.W	#6,D1
	ADD.W	#2,D1
	MOVE.W	D5,D0
	ADD.W	D7,D0
	LSL.W	#2,D7
	MOVE.W	D0,D2
	SUB.W	D6,D2
	ADD.W	D2,D2
	TST.W	D0
	BPL.S	PLUS
	OR.B	#64,BLTC1
PLUS	ADD.W	D0,D0
	EXT.L	D0
WDRAW	BTST	#6,$DFF002
	BNE.S	WDRAW
	CLR	D6
	MOVE.B	BLTC1(PC),D6
	OR.W	#2,D6
	MOVE.W	D6,$DFF042
	MOVE.W	D4,$DFF040
	CLR	$DFF070
	MOVE.W	#$8000,$DFF074
	MOVE.W	#$FFFF,$DFF072
	MOVE.L	#$FFFFFFFF,$DFF044
	MOVE.W	#40,$DFF060
	MOVE.W	#40,$DFF066
	MOVE.W	D7,$DFF062
	MOVE.W	D2,$DFF064
	MOVE.L	D3,$DFF048
	MOVE.L	D3,$DFF054
	MOVE.L	D0,$DFF050
	MOVE.W	D1,$DFF058
DRAWOUT	RTS

** NOTE PLANE MIN AND MAX FOR THIS PLANE **************************************

EXTREM	CMP.W	YMIN(PC),D1
	BHI.B	NOYMIN
	MOVE.W	D1,YMIN
NOYMIN	CMP.W	YMAX(PC),D1
	BLT.B	NOYMAX
	MOVE.W	D1,YMAX
NOYMAX	CMP.W	XMIN(PC),D0
	BHI.B	NOXMIN
	MOVE.W	D0,XMIN
NOXMIN	CMP.W	XMAX(PC),D0
	BLT.B	NOXMAX
	MOVE.W	D0,XMAX
NOXMAX	RTS

** NOTE MIN AND MAX FOR ALL PLANES ********************************************

GLOBAL	MOVE.W	XMIN(PC),D0
	CMP.W	GLOBALXMIN(PC),D0
	BHI.B	NOGLOBX
	MOVE.W	D0,GLOBALXMIN
NOGLOBX	MOVE.W	XMAX(PC),D0
	CMP.W	GLOBALXMAX(PC),D0
	BLT.B	GLOBOY1
	MOVE.W	D0,GLOBALXMAX
GLOBOY1	MOVE.W	YMIN(PC),D0
	CMP.W	GLOBALYMIN(PC),D0
	BHI.B	NOGLOBY
	MOVE.W	D0,GLOBALYMIN
NOGLOBY	MOVE.W	YMAX(PC),D0
	CMP.W	GLOBALYMAX(PC),D0
	BLT.B	GLOBOUT
	MOVE.W	D0,GLOBALYMAX
GLOBOUT	RTS	

** CLACULATE NEW VIEW ANGLE ***************************************************

ANGLES	MOVE.W	YDELTA(PC),D0
	ADD.W	D0,YVINKEL			; NEW Y ANGLE
	CMP.W	#512,YVINKEL
	BLT.B	XANGLE
	SUB.W	#512,YVINKEL
XANGLE	MOVE.W	XDELTA(PC),D0
	ADD.W	D0,XVINKEL			; NEW X ANGLE
	CMP.W	#512,XVINKEL
	BLT.B	ZANGLE
	SUB.W	#512,XVINKEL
ZANGLE	MOVE.W	ZDELTA(PC),D0
	ADD.W	D0,ZVINKEL			; NEW Z ANGLE
	CMP.W	#512,ZVINKEL
	BLT.B	ANGLOUT	
	SUB.W	#512,ZVINKEL
ANGLOUT	RTS

** FLIP BETWEEN FRAMESBUFFERS *************************************************

FLIP_SCREEN
	MOVEQ	#6,D1
	MOVEQ	#2,D2
	MOVE.W	PLANER(PC),D3
	LEA.L	PLANES,A0
	CMP.L 	#SCREEN1,SCREEN_ADRESS		
	BEQ.S	FLIP_TO_SCREEN2
	MOVE.L	#SCREEN2,D0
FLIP1	MOVE.W	D0,(A0,D1.W)
	SWAP 	D0
	MOVE.W	D0,(A0,D2.W)
	SWAP	D0
	ADDQ	#8,D1
	ADDQ	#8,D2	
	ADD.L	#10240,D0
	SUBQ.W	#1,D3
	BNE.B	FLIP1
	MOVE.L	#SCREEN1,SCREEN_ADRESS	
	RTS
FLIP_TO_SCREEN2
	MOVE.L	#SCREEN1,D0
FLIP2	MOVE.W	D0,(A0,D1.W)
	SWAP 	D0
	MOVE.W	D0,(A0,D2.W)
	SWAP	D0
	ADDQ	#8,D1
	ADDQ	#8,D2	
	ADD.L	#10240,D0
	SUBQ.W	#1,D3
	BNE.B	FLIP2
	MOVE.L	#SCREEN2,SCREEN_ADRESS
	RTS

** CLEAR SCREEN ***************************************************************

CLEAR	CMP.W	#320,GLOBALXMIN
	BEQ.W	CLROUT
	MOVE.L	SCREEN_ADRESS(PC),D0
	MOVE.W	GLOBALXMIN(PC),D1	
	MOVE.W	GLOBALXMAX(PC),D2
	SUB.W	D1,D2				; D2 = CLEAR WIDTH IN PIXELS
	EXT.L	D1
	LSR.W	#3,D1				; D1 = LEFT PIXELS IN BYTES
	SUBQ.W	#1,D1
	ADD.L	D1,D0				; D0 = START ADDRESS
	LSR.W	#4,D2				; D2 = CLEAR WIDTH IN WORDS
	ADDQ.W	#3,D2				; FOR SAFETY	
	MOVE.W	D2,D1
	LSL.W	#1,D1				; D1 = CLEAR WIDTH IN BYTES
	MOVE.W	#40,D3
	SUB.W	D1,D3				; D3 = CLEAR MODULO
	MOVE.W	GLOBALYMIN(PC),D4
	SUB.W	SAFETY(PC),D4
	MOVE.W	GLOBALYMAX(PC),D5
	SUB.W	D4,D5				; HIGHT
	ADD.W	SAFETY(PC),D5
	MULU	#40,D4
	EXT.L	D4
	ADD.L	D4,D0
	LSL.W	#6,D5
	OR.W	D5,D2				; HIGHT AND WIDTH
CLR1	BTST	#14,$DFF002
	BNE.S	CLR1
	MOVE.W	#$0100,$DFF040
	MOVE.W	#$0000,$DFF042
	MOVE.L	#$FFFFFFFF,$DFF044
	MOVE.L	D0,$DFF054
	MOVE.W	D3,$DFF066
	MOVE.W	D2,$DFF058
	ADD.L	#10240,D0			; POINT ON NEXT PLANE
	MOVE.W	PLANER(PC),D4
	SUBQ	#1,D4
CLR2	BTST	#14,$DFF002
	BNE.S	CLR2
	MOVE.L	D0,$DFF054
	MOVE.W	D3,$DFF066
	MOVE.W	D2,$DFF058
	ADD.L	#10240,D0			; POINT ON NEXT PLANE
	SUBQ	#1,D4
	BNE.B	CLR2
CLROUT	RTS

** BLITTER FILL (DESCENDING MODE) *********************************************

FILL	MOVE.L	A0,D0				; SCREEN ADDRESS
	MOVE.W	YMAX(PC),D1
	MULU	#40,D1	
	ADD.L	D1,D0				; D0 = BUTTOM VERTICAL LINE
	MOVE.W	#320,D2
	SUB.W	XMAX(PC),D2
	LSR.W	#3,D2				; 16 PIXEL PRECISION
	EXT.L	D2
